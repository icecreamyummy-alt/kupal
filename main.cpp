using namespace std;

int main() { // STARTING POINT  // MAIN INITIALIZE THE VARIABLE HEIGHT
    int height = 8; //GANO KATAAS BAHAY

    // SA BUBONG
    for (int i = 0; i < height; i++) {
        // SPACES SA TAAS NG ROOF
        for (int j = height - i; j > 1; j--) {
            cout << " ";
        }
        // PAG PRINT NG ROOF
        for (int j = 0; j <= i; j++) {
            cout << "*  ";
        }
        cout << endl; // COUT PARA SA NEXT LINE 
    }


    for (int i =0; i < height; i++) {
        // LEADING SPACES NAMAN NG BODY
        for (int j = 0; j < height - 8;j++) {
            cout << " ";
        }
        // BODY OF THE HOUSE
        for (int j = 0; j < height; j++) {
            cout << "*  ";
        }
        cout << endl; 
    }

    

    return 0;
}




































































































































































































































































































































































































































































































































































































































































































































































 